Console.WriteLine("Executando o projeto 1 - Variaveis tipo inteiro.");

//Declarando uma variavel de numeros inteiros
int idade;

//Atribuindo um valor a variavel 
idade = 10;

Console.WriteLine("Valor da idade: " + idade);

//Atribuindo usando matematica
idade = 5 + 10 * 2;
Console.WriteLine(idade);

//Mudando a ordem
idade = (5 + 10) * 2;
Console.WriteLine(idade);

//Somando 5 em idade
idade += 5;
idade = idade + 5;
Console.WriteLine(idade);

//Subtração 5 em idade
idade -= 5;
Console.WriteLine(idade);

//Multiplicando 5 em idade
idade *= 5;
Console.WriteLine(idade);

//Dividindo 5 em idade 
idade /= 5;
Console.WriteLine(idade);

//Outra forma de exibir uma variavel 
Console.WriteLine($"Sua idade é: {idade}!");

Console.WriteLine("Sua idade é: " + idade + "!");



